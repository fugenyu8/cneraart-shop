/**
 * 生成面相/手相解读报告（本地模板引擎）
 */
export async function generateAIInterpretation(
  calculationResult: CalculationResult,
  serviceType: "face" | "palm",
  faceType?: string,
  handType?: string
): Promise<AIInterpretationResult> {
  const sections: AIInterpretationResult["sections"] = [];
  let totalScore = 0;
  let count = 0;

  // 按分数排序，高分在前
  const sorted = Object.entries(calculationResult).sort(
    (a, b) => b[1].score - a[1].score
  );

  for (const [name, data] of sorted) {
    const templates =
      serviceType === "face"
        ? FACE_PALACE_TEMPLATES
        : { ...PALM_LINE_TEMPLATES, ...PALM_HILL_TEMPLATES };

    const content = getInterpretation(templates, name, data.category, data.interpretations);
    totalScore += data.score;
    count++;

    sections.push({
      title: `${name}分析`,
      content,
      score: data.score,
      category: data.category,
    });
  }

  const avgScore = count > 0 ? Math.round(totalScore / count) : 65;
  const overallCategory = getOverallCategory(avgScore);

  // 生成综合总结
  const overallTemplates = serviceType === "face" ? FACE_OVERALL_TEMPLATES : PALM_OVERALL_TEMPLATES;
  const overallTexts = overallTemplates[overallCategory as keyof typeof overallTemplates] || overallTemplates["平"];
  const overallSummary = overallTexts[Math.floor(Math.random() * overallTexts.length)];

  // 添加面型/手型性格分析
  if (serviceType === "face" && faceType && FACE_TYPE_PERSONALITY[faceType]) {
    sections.unshift({
      title: "面型分析",
      content: FACE_TYPE_PERSONALITY[faceType],
      score: avgScore,
      category: overallCategory,
    });
  }
  if (serviceType === "palm" && handType && HAND_TYPE_PERSONALITY[handType]) {
    sections.unshift({
      title: "手型分析",
      content: HAND_TYPE_PERSONALITY[handType],
      score: avgScore,
      category: overallCategory,
    });
  }

  // 生成建议
  const adviceTemplates = ADVICE_TEMPLATES[serviceType] || ADVICE_TEMPLATES.face;
  const adviceSection = adviceTemplates[overallCategory as keyof typeof adviceTemplates] || adviceTemplates["平"];

  // 幸运元素
  const luckyElements = getLuckyElements(faceType, handType);

  return {
    overallSummary,
    sections,
    faceType,
    handType,
    overallScore: avgScore,
    overallCategory,
    adviceSection,
    luckyElements,
  };
}

function getOverallCategory(score: number): string {
  if (score >= 90) return "大吉";
  if (score >= 75) return "吉";
  if (score >= 65) return "中吉";
  if (score >= 50) return "平";
  if (score >= 35) return "小凶";
  return "凶";
}

function getInterpretation(
  templates: Record<string, Record<string, string[]>>,
  name: string,
  category: string,
  ruleInterpretations: string[]
): string {
  // 优先使用模板文案
  if (templates[name]) {
    // 映射六级到模板键
    const templateKey = mapCategoryToTemplateKey(category);
    const texts = templates[name][templateKey];
    if (texts && texts.length > 0) {
      const templateText = texts[Math.floor(Math.random() * texts.length)];
      // 如果有规则匹配的解读，附加在模板文案后面
      if (ruleInterpretations.length > 0) {
        const ruleText = ruleInterpretations.slice(0, 3).join("。");
        return `${templateText}\n\n【细节分析】${ruleText}。`;
      }
      return templateText;
    }
  }

  // 如果没有模板，使用规则匹配的解读
  if (ruleInterpretations.length > 0) {
    return ruleInterpretations.join("。") + "。";
  }

  return `${name}整体表现${category}，运势平稳。`;
}

function mapCategoryToTemplateKey(category: string): string {
  switch (category) {
    case "大吉": return "大吉";
    case "吉": return "吉";
    case "中吉": return "中吉";
    case "平": return "平";
    case "小凶": return "小凶";
    case "凶": return "凶";
    default: return "平";
  }
}

/**
 * 带重试的报告生成（兼容旧接口）
 */
export async function generateAIInterpretationWithRetry(
  calculationResult: CalculationResult,
  serviceType: "face" | "palm",
  faceType?: string,
  handType?: string
): Promise<AIInterpretationResult> {
  return generateAIInterpretation(calculationResult, serviceType, faceType, handType);
}
