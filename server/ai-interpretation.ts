function getLuckyElements(faceType?: string, handType?: string): {
  color: string;
  number: string;
  direction: string;
  element: string;
} {
  const type = faceType || handType || "土型脸";
  if (type.includes("金")) return { color: "白色、银色", number: "4、9", direction: "西方", element: "金" };
  if (type.includes("木")) return { color: "绿色、青色", number: "3、8", direction: "东方", element: "木" };
  if (type.includes("水")) return { color: "黑色、蓝色", number: "1、6", direction: "北方", element: "水" };
  if (type.includes("火")) return { color: "红色、紫色", number: "2、7", direction: "南方", element: "火" };
  return { color: "黄色、棕色", number: "5、10", direction: "中央", element: "土" };
}

